def cal_FaceNormal(p0, p1, p2):
    # Convert vertices to numpy arrays
    A = np.array(bigship_vertices[p0], dtype=float)
    B = np.array(bigship_vertices[p1], dtype=float)
    C = np.array(bigship_vertices[p2], dtype=float)

    # Edge vectors
    u = B - A
    v = C - A

    # Cross product
    n = np.cross(u, v)

    # Normalize
    norm = np.linalg.norm(n)
    if norm == 0:
        raise ValueError("Degenerate triangle (zero area)")

    n_unit = n / norm
    return n_unit

def cal_VertexNormal(arTris):
	nTris = len(arTris)

	# Each Tris is a tuple (p0, p1, p2),
	# p0 is the index into bigship_vertices[] array. So is p1, p2.

	accum_Normal = np.array([0, 0, 0], dtype=float)

	for i, tri in enumerate(arTris):
		thisN = cal_FaceNormal(tri[0], tri[1], tri[2])
		accum_Normal += thisN

	norm = np.linalg.norm(accum_Normal)
	n_unit = accum_Normal / norm
	print("VertexNormal =", n_unit)


